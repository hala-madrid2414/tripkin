type BottleSeed = Omit<
  BottleMessage,
  | 'bottleType'
  | 'authorAvatar'
  | 'destinationName'
  | 'images'
  | 'isLiked'
  | 'isCollected'
  | 'isFollowing'
> & {
  avatarSeed: number
  bottleType?: BottleVisibilityType
  destinationName?: string
  images?: string[]
  isLiked?: boolean
  isCollected?: boolean
  isFollowing?: boolean
}

